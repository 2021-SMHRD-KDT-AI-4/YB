package com.example.findyourdog;

public class FindItem {

    String board_num;
    String id;
    String board_type;
    String status;
    String picture;
    String gender;
    String age;
    String color;
    String kind;
    String weight;
    String missing_date;
    String missing_time;
    String notice;
    String shelter;
    String city;
    String place;
    String tel;
    String content;

    public FindItem(String board_num, String id, String board_type, String status, String picture, String gender, String age, String color, String kind, String weight, String missing_date, String missing_time, String notice, String shelter, String city, String place, String tel, String content) {
        this.board_num = board_num;
        this.id = id;
        this.board_type = board_type;
        this.status = status;
        this.picture = picture;
        this.gender = gender;
        this.age = age;
        this.color = color;
        this.kind = kind;
        this.weight = weight;
        this.missing_date = missing_date;
        this.missing_time = missing_time;
        this.notice = notice;
        this.shelter = shelter;
        this.city = city;
        this.place = place;
        this.tel = tel;
        this.content = content;
    }

    public String getBoard_num() {
        return board_num;
    }

    public void setBoard_num(String board_num) {
        this.board_num = board_num;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBoard_type() {
        return board_type;
    }

    public void setBoard_type(String board_type) {
        this.board_type = board_type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getMissing_date() {
        return missing_date;
    }

    public void setMissing_date(String missing_date) {
        this.missing_date = missing_date;
    }

    public String getMissing_time() {
        return missing_time;
    }

    public void setMissing_time(String missing_time) {
        this.missing_time = missing_time;
    }

    public String getNotice() {
        return notice;
    }

    public void setNotice(String notice) {
        this.notice = notice;
    }

    public String getShelter() {
        return shelter;
    }

    public void setShelter(String shelter) {
        this.shelter = shelter;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
